export async function generateMetadata({
  params,
}: {
  params: Promise<{ lang: string }>;
}): Promise<Metadata> {
  const { lang } = await params;
  const page = content[lang as keyof typeof content] || content.bg;

  return {
    title: page.title,
    description: page.description,
    alternates: {
      canonical: `/${lang}/payments-refunds`,
      languages: {
        bg: "/bg/payments-refunds",
        ru: "/ru/payments-refunds",
      },
    },
    openGraph: {
      title: page.title,
      description: page.description,
      url: `${BASE_URL}/${lang}/payments-refunds`,
      type: "website",
    },
  };
}

export default async function PaymentsRefundsPage({
  params,
}: {
  params: Promise<{ lang: string }>;
}) {
  const { lang } = await params;
  const page = content[lang as keyof typeof content] || content.bg;

  return <LegalPage {...page} />;
}
